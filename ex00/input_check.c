int	input_check(int argc, char *pov)
{
	int	i;

	i = 0;
	if (argc != 2)
		return (1);
	while (pov[i] != '\0')
	{
		if (i % 2 == 0)
		{
			if (pov[i] < '1' || pov[i] > '4')
				return (1);
		}
		else
		{
			if (pov[i] != ' ')
				return (1);
		}
		i++;
	}
	if (i != 31)
		return (1);
	return (0);
}
